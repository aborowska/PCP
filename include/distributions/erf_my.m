function val = erf_my(x)
    sgn = sign(x); 
    x = abs(x);
    y = 1.0 ./ ( 1.0 + 0.3275911 .* x);   
    val = 1 - (((((1.061405429*y...
        - 1.453152027) .* y...
        + 1.421413741) .* y...
        - 0.284496736) .* y ...
        + 0.254829592) .* y) ...
        .* exp (-x.*x);    
    
    val = sgn.*val;

end